/**
 * Get a team object by its code
 * Returns a fallback object if the code isn't found
 * so components never crash on unknown teams
 */
export const getTeam = (code) => {
  return (
    TEAMS[code] ?? {
      id: code,
      name: code === "TBD" ? "TBD" : code,
      code,
      confederation: "Unknown",
      flag: "🏳️",
    }
  );
};

/**
 * Get all fixtures for a specific group
 */
export const getFixturesByGroup = (groupKey) => {
  return FIXTURES.filter((f) => f.group === groupKey);
};

/**
 * Get only live fixtures across all groups
 */
export const getLiveFixtures = () => {
  return FIXTURES.filter((f) => f.status === "live");
};

/**
 * Get upcoming fixtures — optionally limit how many
 */
export const getUpcomingFixtures = (limit = 5) => {
  return FIXTURES.filter((f) => f.status === "upcoming").slice(0, limit);
};

/**
 * Format a match scoreline as a string
 * e.g. "2 - 1" or "vs" if not yet played
 */
export const formatScore = (home, away) => {
  if (home === null || away === null) return "vs";
  return `${home} - ${away}`;
};

/**
 * Determine the winner code of a knockout match
 * Returns null if the match isn't finished
 */
export const getWinner = (match) => {
  if (match.status !== "finished") return null;
  if (match.homeScore > match.awayScore) return match.home;
  if (match.awayScore > match.homeScore) return match.away;
  return null; // draw (shouldn't happen in knockout)
};
